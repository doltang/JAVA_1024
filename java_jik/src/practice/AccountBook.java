package practice;

public class AccountBook {
	private String inEx;
	private String type;
	private String content;
	private int money;
	private int year;
	private int month;
	private int day;
	
	
	public AccountBook(String inEx, String type, String content, int money, int year, int month, int day) {
		this.inEx = inEx;
		this.type = type;
		this.content = content;
		this.money = money;
		this.year = year;
		this.month = month;
		this.day = day;
	}
	
}
