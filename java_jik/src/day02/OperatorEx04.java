package day02;

public class OperatorEx04 {

	public static void main(String[] args) {
		/* 비교 연산자 : 크기 비교
		 * - 결과 값이 참 또는 거짓이 나옴
		 * 조건식 : 연산 결과가 참 또는 거짓이 나오는 식
		 * */
		int age = 17; 
		System.out.println(age + "살은 성인인가? " + (age >= 19)); // false
		
		int score = 100;
		System.out.println(score + "점은 A학점인가? " + (score >= 90)); // true
		
	}

}
